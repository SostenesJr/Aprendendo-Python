"""
Python = Liguagem de programação
Tipo de Tipagem = Dinâmica / Forte
str -> String -> Texto
Strings são`textos que são dentro de aspas
"""

print(1234)  # Tipagem dinamica porque ele sabe
# que é um numero sem precisa passa essa informação

# Aspas simples
print('Luiz Otávio')
print('1, "Luiz Otávio"')

# Aspas duplas
print("Luiz Otávio")

# Escape
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio\"")
