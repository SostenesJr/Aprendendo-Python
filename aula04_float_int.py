"""
Tipo int e float
int -> Número inteiro
positivo ou negativo. int sem sinal é considerado positivo
"""
print(11)  # int inteiro
print(-11)  # int inteiro
print(0)  # int inteiro
"""
Float -> Número com ponto flutuante
o tipo float representa qualquer número
positivo ou negativo com ponto flutuante.
float sem fnal é considerado positivo
"""
print(0.0)  # int float quebrado
print(10.10)  # int float quebrado

# A função mostra o tipo que o python inferiu ao valor
print(type('Otávio'))
print(type(1))
print(type(1.1), type(-1.1), type(0.0))
